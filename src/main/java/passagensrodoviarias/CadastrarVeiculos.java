    private void btCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btCadastrarActionPerformed
        String numero = campoNumero.getText();
        String placa = campoPlaca.getText();
        String motorista = campoMotorista.getText();
        String modelo = campoModelo.getText();
        int qtdPoltronas = Integer.parseInt(campoPoltronas.getText());

        try (Connection conn = DriverManager.getConnection(URL, USER, PASSWORD)) {
            
            java.sql.Date dataAtual = new java.sql.Date(System.currentTimeMillis());
            
            String sql = "INSERT INTO veiculos (numero, placa, motorista, modelo, qtd_poltronas, data_compra) VALUES (?, ?, ?, ?, ?, ?)";
            try (PreparedStatement pstmt = conn.prepareStatement(sql)) {
                pstmt.setString(1, numero);
                pstmt.setString(2, placa);
                pstmt.setString(3, motorista);
                pstmt.setString(4, modelo);
                pstmt.setInt(5, qtdPoltronas);
                pstmt.setDate(6, dataAtual); // Fornecendo a data atual para a coluna data_compra
                pstmt.executeUpdate();
            }
            System.out.println("Veículo cadastrado com sucesso!");

            dispose();

        } catch (SQLException ex) {
            System.out.println("Erro ao cadastrar veículo: " + ex.getMessage());
        }
    }//GEN-LAST:event_btCadastrarActionPerformed

